idade = [10, 61, 2, 87, 23]

idade.append(18) #adiciona na ultima posicao
print(idade)

idade.insert(1, 1) #insere um novo dado
print(idade)

idade.pop(0) #remove pelo indice
print(idade)

idade.remove(2) #remove pelo valor
print(idade)

print(len(idade)) #quantidade de dados na lista
print(idade)

idade.sort(reverse= True) #ordenacao
print(idade)

idade.reverse() #pega o untimo indice e conta como o primeiro
print(idade)